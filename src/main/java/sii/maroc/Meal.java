package sii.maroc;

public class Meal {
	 int duration;
	 int number;
	public int getDuration() {
		return duration;
	}

	public void setDuration(int duration) {
		this.duration = duration;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public int servedDishes() {
		return number;
	}

	public int cookingDuration() {
		return duration;
	}

}
